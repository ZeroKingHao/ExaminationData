function estimateRank(year, score) {
  const table = scoreToRank[year];
  if (!table) return 0;
  for (let i = 0; i < table.length - 1; i++) {
    if (score > table[i+1][0] && score <= table[i][0]) {
      const ratio = (score - table[i][0]) / (table[i+1][0] - table[i][0]);
      return Math.round(table[i][1] + ratio * (table[i+1][1] - table[i][1]));
    }
  }
  for (const [s, r] of table) {
    if (score === s) return r;
  }
  return 0;
}

function extractYear(filePath, year) {
  console.log(`\n===== ${year} =====`);
  const wb = XLSX.readFile(filePath);
  const ws = wb.Sheets[wb.SheetNames[0]];
  const rows = XLSX.utils.sheet_to_json(ws, { header: 1, defval: '' });
  console.log(`Total rows: ${rows.length}`);

  const results = [];
  // 数据从第6行开始（row index 5），列结构：col1=院校名称, col3=专业名称, col4=投档最低分
  for (let i = 5; i < rows.length; i++) {
    const row = rows[i];
    const uniRaw = String(row[1] || '');
    const tier = getTier(uniRaw);
    if (!tier) continue;

    const major = String(row[3] || '').trim();
    const minScore = parseInt(row[4]) || 0;
    if (minScore === 0) continue;

    const minRank = estimateRank(year, minScore);
    const uniClean = getCleanName(uniRaw);

    results.push({ year, university: uniClean, major, tier, minScore, minRank, source: 'hebeea.edu.cn' });
  }

  console.log(`Extracted ${results.length} records`);
  const unis = new Set(results.map(r => r.university));
  console.log(`${unis.size} universities (985: ${results.filter(r=>r.tier==='985').length}, 211: ${results.filter(r=>r.tier==='211').length})`);

  // 样本
  [...unis].slice(0, 8).forEach(u => {
    const samples = results.filter(r => r.university === u);
    console.log(`  ${u}: ${samples.length} majors, score range ${Math.min(...samples.map(s=>s.minScore))}-${Math.max(...samples.map(s=>s.minScore))}`);
  });

  return results;
}

