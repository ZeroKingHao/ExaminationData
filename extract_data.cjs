function getTier(name) {
  for (const k of K985) if (name.includes(k)) return '985';
  for (const k of K_CAMPUSES) if (name.includes(k)) return '985';
  for (const k of K211) if (name.includes(k)) return '211';
  return null;
}

function getCleanName(name) {
  // 去掉 [公办] 等标注
  name = name.replace(/\[.*?\]/g, '').replace(/[\(\)]/g, (m) => m).trim();
  const all = [...K_CAMPUSES, ...K985, ...K211];
  for (const k of all) {
    if (name.includes(k)) return k;
  }
  return name;
}

